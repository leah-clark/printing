package com.exercise.Domain;

public class Hsv {
    private FractionHsv fraction;
    private int h;
    private int s;
    private int v;
    private String value;

    public FractionHsv getFraction() {
        return fraction;
    }

    public int getH() {
        return h;
    }

    public int getS() {
        return s;
    }

    public int getV() {
        return v;
    }

    public String getValue() {
        return value;
    }
}
